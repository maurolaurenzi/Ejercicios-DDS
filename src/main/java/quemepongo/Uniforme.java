package quemepongo;

public class Uniforme {
	private Prenda parteSuperior;
	private Prenda parteInferior;
	private Prenda calzado;
	
	public Uniforme(Prenda _parteSup, Prenda _parteInf, Prenda _calzado) {
		super();
		this.parteSuperior = _parteSup;
		this.parteInferior = _parteInf;
		this.calzado = _calzado;
		
	}
}
